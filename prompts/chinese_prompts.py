PAST_MESSAGE_TRUNCATION_LIMIT = 300


def _truncate_content(text, limit=PAST_MESSAGE_TRUNCATION_LIMIT):
    """将文本截断到限制字符数，缩短时附加"..."。"""
    if len(text) <= limit:
        return text
    return text[:limit] + "..."


def _format_summary(summary):
    """从字符串或带有"summary"键的字典中提取摘要文本。"""
    if isinstance(summary, dict):
        return summary.get("summary", "")
    return summary or ""


def _format_conversation_history(messages):
    """将消息字典格式化为"角色: 内容"行，并进行截断。"""
    if not messages:
        return ""
    result = ""
    for msg in messages:
        role = msg.get("role", "")
        content = msg.get("message") or msg.get("content", "")
        if role and content:
            result += f"{role}: {_truncate_content(content)}\n"
    return result


def _serialize_memories(memories):
    """JSON序列化记忆对象列表，默认为"[]"。"""
    return json.dumps(memories or [], ensure_ascii=False)


def _format_new_messages(new_messages):
    """如果已经是字符串则直接返回，否则JSON序列化。"""
    if isinstance(new_messages, str):
        return new_messages
    return json.dumps(new_messages or [], ensure_ascii=False)


def _resolve_dates(current_date=None, observation_date=None):
    """解析当前和观察日期，默认为今天。"""
    if current_date is None:
        current_date = datetime.now(timezone.utc).date().isoformat()
    if observation_date is None:
        observation_date = current_date
    return current_date, observation_date


def generate_additive_extraction_prompt(
    summary=None,
    recently_extracted_memories=None,
    existing_memories=None,
    new_messages=None,
    *,
    last_k_messages=None,
    current_date=None,
    timestamp=None,
    custom_instructions=None,
    use_input_language=False,
):
    """为带链接的附加（仅ADD）提取构建用户提示。

    与ADDITIVE_EXTRACTION_PROMPT系统提示配对使用。
    LLM将仅生成ADD操作，附带可选的linked_memory_ids。
    """
    current_date, observation_date = _resolve_dates(current_date, timestamp)

    sections = []
    sections.append(f"## 摘要\n{_format_summary(summary)}")
    sections.append(f"## 最近k条消息\n{_format_conversation_history(last_k_messages)}")
    sections.append(f"## 最近提取的记忆\n{_serialize_memories(recently_extracted_memories)}")
    sections.append(f"## 现有记忆\n{_serialize_memories(existing_memories)}")
    sections.append(f"## 新消息\n{_format_new_messages(new_messages)}")
    sections.append(f"## 观察日期\n{observation_date}")
    sections.append(f"## 当前日期\n{current_date}")

    if custom_instructions:
        sections.append(f"## 自定义指令\n{custom_instructions}")

    if use_input_language:
        sections.append(
            "## 语言要求\n"
            "关键：使用与输入消息**相同的语言和文字**响应。\n"
            "1. 完全匹配用户消息的语言 -- 如果用户用韩语书写，用韩语提取；日语用日语；依此类推。\n"
            "2. 保留输入的确切文字/字母表。\n"
            "3. 不要翻译或音译为英语，除非输入已经是英语。\n"
            "4. 无论语言如何，保持所有质量标准（上下文丰富性、时间落地等）。\n"
            "5. 技术术语、专有名词和品牌名称应按照输入中的原始形式保留。\n"
            "6. 如果输入混合语言（例如：印地式英语），同时保留混合语言风格和文字。\n"
            "7. 对于日语：使用对话上下文明确解析省略的主语。\n"
            "8. 对于中日韩语言：保持与源文本相适应的正式程度。"
        )

    sections.append("# 输出：")
    return "\n\n".join(sections)
